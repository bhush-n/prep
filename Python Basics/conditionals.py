# IF-Else
# if condition:
#     statement
# else:
#     statement

age = 20

if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# Elif
# if condition:
#     statement
# elif condition:
#     statement
# else:
#     statement

score = 85
if score >= 90:
    print("Grade: A")
elif score >= 80:
    print("Grade: B")
elif score >= 70:
    print("Grade: C")
elif score >= 60:
    print("Grade: D")
else:
    print("Grade: F")