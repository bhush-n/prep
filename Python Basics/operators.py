# Arithmetic operators
print(5 + 3)  # 8
print(5 - 3)  # 2
print(5 * 3)  # 15
print(5 / 3)  # 1.6666666666666667
print(5 // 3)  # 1
print(5 % 3)  # 2
print(5 ** 3)  # 125

# Conditional operators
print(5 > 3)  # True
print(5 < 3)  # False
print(5 >= 3)  # True
print(5 <= 3)  # False
print(5 == 3)  # False
print(5 != 3)  # True


# Logical operators
print(True and False)  # False
print(True or False)  # True
print(not True)  # False
print(not False)  # True

# Assignment operators
x = 5
x += 3  # x = x + 3
print(x)  # 8
x -= 3  # x = x - 3
print(x)  # 5
